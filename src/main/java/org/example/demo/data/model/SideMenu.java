package org.example.demo.data.model;

public class SideMenu {
    private String title;

    public SideMenu(String title) {
        this.title = title;
    }


    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    @Override
    public String toString() {
        return title;
    }
}
